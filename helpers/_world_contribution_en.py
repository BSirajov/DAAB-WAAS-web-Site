def contribution_to_work_desc(en_contribution: str) -> str:
    """Turn a contribution clause into a standalone work-desc sentence."""
    if en_contribution.startswith("being "):
        return en_contribution[0].upper() + en_contribution[1:] + "."
    if en_contribution[0].islower():
        return en_contribution[0].upper() + en_contribution[1:] + "."
    return en_contribution if en_contribution.endswith(".") else en_contribution + "."


def translate_work_desc_az(az_text: str) -> str | None:
    """Map AZ work-desc lines to EN using contribution dictionary."""
    core = az_text.strip().rstrip(".")
    core = core.translate(str.maketrans({
        "\u201c": '"', "\u201d": '"',
        "\u2018": "'", "\u2019": "'",
    }))
    lower = core.lower()
    for az_key, en_val in WORLD_CONTRIBUTION_EN.items():
        short = az_key[:-7] if az_key.endswith(" olması") else az_key
        short = short.translate(str.maketrans({
            "\u201c": '"', "\u201d": '"',
            "\u2018": "'", "\u2019": "'",
        }))
        if lower == short.lower():
            return contribution_to_work_desc(en_val)
    return None
